export function getAllRegionNames(): string[] {
  const names = new Set<string>();
  regionAliases.forEach((entry) => {
    names.add(entry.canonical);
    entry.aliases.forEach((a) => names.add(a));
    entry.parentRegions?.forEach((p) => names.add(p));
    entry.subRegions?.forEach((s) => names.add(s));
  });
  return Array.from(names);
}

export function getAllGrapeNames(): string[] {
  const names = new Set<string>();
  grapeAliases.forEach((entry) => {
    names.add(entry.canonical);
    entry.aliases.forEach((a) => names.add(a));
  });
  return Array.from(names);
}
